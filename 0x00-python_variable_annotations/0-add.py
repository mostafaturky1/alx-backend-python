#!/usr/bin/env python3

def add(a: float, b: float) -> float:
    """
    This function takes two float numbers as input and returns their sum.

    Parameters:
    a (float): The first number to be added.
    b (float): The second number to be added.

    Returns:
    float: The sum of the input numbers.

    Example:
    >>> add(1.5, 2.7)
    4.2
    """
    return a + b
